# DATA TYPES IN PYTHON 
# INTEGER
num1 = 10
print("Type:", type(num1))

# FLOAT
num2 = 10.5
print( " Type:", type(num2))

# STRING
name = "Sujit Pawar "
print("Type:", type(name))

# BOOLEAN
x = True
print("Type:", type(x))

#OPERATORS IN PYTHON
# ARITHMATIC OPERATORS
a = 15
b = 4
print("Division:", a / b) # Division

print("Modulus:", a % b) # Modulus

print("Addition:",a+b) #Addition

print("Multiplication:",a*b) #Multiplication

print("Substraction:",a-b) #Substraction

# COMPARISON OPERATORS
print("Greater than:", a > b) # Greater than

print("Equal to:", a == b) # Equality check

print("Less than:", a < b) #Less than

print("Less than equal to:",a<=b)#less than equal to

print("Greater than equal to:",a>=b)#greater than equal to

print("Not equal:",a!=b)# not equal to
